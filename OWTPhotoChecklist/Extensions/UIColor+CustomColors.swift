import UIKit

extension UIColor
{
    public class func cfoColor(alpha alpha: CGFloat = 1.0) -> UIColor
    {
        return UIColor(red: 245.0 / 255.0, green: 166.0 / 255.0, blue: 35.0 / 255.0, alpha: alpha)
    }
}